/* 
¿Qué es un evento?
Los eventos son cosas que suceden en el sistema que estás programando. El 
sistema se encarga de producir una señal de cierto tipo cuando un evento 
ocurre, y proporciona un mecanismo para que una acción se lleve a cabo 
(ejecutar código) de forma automática cuando el evento ocurra. Los eventos se 
lanzan dentro de la ventana del navegador y usualmente están asociados a un 
elemento en específico dentro de dicha ventana. Esto puede ser un solo 
elemento, un grupo de elementos, el documento HTML cargado la pestaña actual, 
o la ventana del navegador en su totalidad. Existen distintos tipos de eventos 
que pueden ocurrir.

Por ejemplo:

- El usuario selecciona, hace clic o pasa el ratón por encima de cierto elemento.
- El usuario presiona una tecla del teclado.
- El usuario redimensiona o cierra la ventana del navegador.
- Una página web terminó de cargarse.
- Un formulario fue enviado.
- Un vídeo se reproduce, se pausa o termina.
- Ocurrió un error.
*/